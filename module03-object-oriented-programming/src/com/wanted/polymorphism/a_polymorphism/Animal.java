package com.wanted.polymorphism.a_polymorphism;

public class Animal {

    public void eat() {
        System.out.println("동물이 먹이를 먹습니다.");
    }

    public void run() {
        System.out.println("동물이 초원을 뛰어다닙니다.");
    }

    public void bark() {
        System.out.println("동물이 슬퍼 울부짖습니다.");
    }

}
